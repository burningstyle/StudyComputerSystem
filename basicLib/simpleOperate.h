/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  int_swap_byXOR
 *  Description:  
 * =====================================================================================
 */
void
int_swap_byXOR (int *x,int *y)
{
        *y=*x ^ *y;
        *x=*x ^ *y;
        *y=*x ^ *y;
}


/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  int_swap
 *  Description:  
 * =====================================================================================
 */
void
int_swap (int *x,int *y)
{
    int temp=0;
    temp=*x;
    *x=*y;
    *y=temp;
}	
